# 별칭(+버전 등) 매핑 생성
ALIAS_MAP = {}
for name, data in RUNE_DATA.items():
    if "별칭" in data:
        for alias in data["별칭"]:
            ALIAS_MAP[alias.lower()] = name

def search_rune(query: str):
    """룬 이름으로 검색"""
    query = query.strip()
    query_lower = query.lower()

    # 1. 정확히 일치하는 룬 찾기
    for name, data in RUNE_DATA.items():
        if name.lower() == query_lower:
            return name, data

    # 2. 별칭으로 찾기
    if query_lower in ALIAS_MAP:
        name = ALIAS_MAP[query_lower]
        return name, RUNE_DATA[name]

    # 3. 포함 검색
    results = []
    for name, data in RUNE_DATA.items():
        if query_lower in name.lower():
            results.append((name, data))
    
    if len(results) == 1:
        return results[0]
    elif len(results) > 1:
        return None, results  # 여러 결과 반환
    
    return None, None

def format_rune_info(name: str, data: dict) -> str:
    """룬 정보를 카카오톡 메시지 형식으로 포맷"""
    등급_이모지 = {
        "에픽": "🟣",
        "전설": "🟠",
        "신화": "🔴",
        "전설 ★8": "⭐",
        "엘리트~전설 ★8": "⭐",
        "에픽~전설 ★8": "⭐",
    }
    
    등급 = data.get("등급", "")
    분류 = data.get("분류", "")
    효과 = data.get("효과", "")
    획득 = data.get("획득", "")
    
    이모지 = 등급_이모지.get(등급, "⚪")
    
    msg = f"{이모지} [{name}]\n"
    msg += f"📊 등급: {등급}\n"
    msg += f"🏷️ 분류: {분류}\n"
    if 획득:
        msg += f"📦 획득: {획득}\n"
    msg += f"📝 효과:\n{효과}"
    
    return msg
